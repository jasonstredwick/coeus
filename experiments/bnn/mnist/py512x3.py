import tensorflow
(x_train, y_train), (x_test, y_test) = tensorflow.keras.datasets.mnist.load_data()
x_train_f = (x_train / 255.0).astype('float32')
y_train_e = tensorflow.keras.utils.to_categorical(y_train)
x_test_f = (x_test / 255.0).astype('float32')
y_test_e = tensorflow.keras.utils.to_categorical(y_test)
model = tensorflow.keras.Sequential()
model.add(tensorflow.keras.Input(shape=(28, 28)))
model.add(tensorflow.keras.layers.Flatten())
model.add(tensorflow.keras.layers.Dense(512, activation=tensorflow.nn.leaky_relu))
model.add(tensorflow.keras.layers.Dense(512, activation=tensorflow.nn.leaky_relu))
model.add(tensorflow.keras.layers.Dense(512, activation=tensorflow.nn.leaky_relu))
model.add(tensorflow.keras.layers.Dense(10, activation=tensorflow.nn.softmax))
model.compile(optimizer='sgd', loss='mse', metrics=['categorical_accuracy'])
history = model.fit(x_train_f, y_train_e, batch_size=32, epochs=50)
model.evaluate(x_test_f, y_test_e)
